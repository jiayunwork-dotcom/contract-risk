    @Override
    public void run(String... args) {
        if (riskRuleRepository.countByEnabledTrue() > 0) {
            log.info("风险规则已存在，跳过初始化");
            return;
        }

        log.info("开始初始化风险规则库...");
        List<RiskRule> rules = buildDefaultRules();
        riskRuleRepository.saveAll(rules);
        log.info("风险规则库初始化完成，共{}条规则", rules.size());
    }

