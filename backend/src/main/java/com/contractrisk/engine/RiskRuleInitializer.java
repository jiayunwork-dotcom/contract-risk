    private RiskRule createRule(String ruleName, String matchPattern, String keywords,
                                 RiskLevel riskLevel, String riskDescription, String suggestion,
                                 String alternativePhrases, String category) {
        RiskRule rule = new RiskRule();
        rule.setRuleName(ruleName);
        rule.setMatchPattern(matchPattern);
        rule.setKeywords(keywords);
        rule.setRiskLevel(riskLevel);
        rule.setRiskDescription(riskDescription);
        rule.setSuggestion(suggestion);
        rule.setAlternativePhrases(alternativePhrases);
        rule.setRuleCategory(category);
        rule.setEnabled(true);
        rule.setMatchMode("REGEX_KEYWORD");
        return rule;
    }
